# --------------------------------------------------
# Intro to Data Types
# --------------------------------------------------
"""
Data types define the kind of value a variable can hold.
Python is dynamically typed, meaning the data type is
determined at runtime.
"""

# --------------------------------------------------
# String
# --------------------------------------------------
"""
String: Represents textual data.
"""
text = "Hello Python"

# --------------------------------------------------
# Numbers (Integer)
# --------------------------------------------------
"""
Integer: Represents whole numbers.
"""
count = 10

# --------------------------------------------------
# Float
# --------------------------------------------------
"""
Float: Represents decimal numbers.
"""
price = 99.99

# --------------------------------------------------
# Boolean
# --------------------------------------------------
"""
Boolean: Represents True or False values.
"""
is_active = True

# --------------------------------------------------
# Complex
# --------------------------------------------------
"""
Complex: Represents complex numbers with real and imaginary parts.
"""
complex_number = 3 + 4j

# --------------------------------------------------
# List
# --------------------------------------------------
"""
List: Ordered, mutable collection of elements.
"""
numbers_list = [1, 2, 3, 4]

# --------------------------------------------------
# Tuple
# --------------------------------------------------
"""
Tuple: Ordered, immutable collection of elements.
"""
coordinates = (10, 20)

# --------------------------------------------------
# Dictionary (Dict)
# --------------------------------------------------
"""
Dictionary: Collection of key-value pairs.
"""
user = {"name": "Ritik", "age": 19}

# --------------------------------------------------
# Set
# --------------------------------------------------
"""
Set: Unordered collection of unique elements.
"""
unique_values = {1, 2, 3}

# --------------------------------------------------
# Frozenset
# --------------------------------------------------
"""
Frozenset: Immutable version of a set.
"""
frozen_values = frozenset([1, 2, 3])

# --------------------------------------------------
# NoneType
# --------------------------------------------------
"""
NoneType: Represents absence of a value.
"""
result = None

# --------------------------------------------------
# Bytes
# --------------------------------------------------
"""
Bytes: Immutable sequence of bytes.
"""
byte_data = b"ABC"

# --------------------------------------------------
# Bytearray
# --------------------------------------------------
"""
Bytearray: Mutable sequence of bytes.
"""
byte_array_data = bytearray(b"ABC")

# --------------------------------------------------
# Object
# --------------------------------------------------
"""
Object: Base class of all Python data types.
Everything in Python is an object.
"""
sample_object = object()

